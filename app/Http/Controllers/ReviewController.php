     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(RequestReview $requestReview, $id)
    {
        if(isset(Auth::get()->id))
        {
            return redirect(route('login'));
        }
        $review = new Review();
        $review->review_comment = $requestReview->review_comment;
        $review->review_star = $requestReview->review_star;
        $review->product_id = $id;
        $review->user_id = Session::get('id_login');
        $review->save();

        return redirect(route('get_products_detail', $id));
    }

